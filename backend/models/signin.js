const mongoose1=require('mongoose');

const user=new mongoose1.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    mobile: {
        type: Number,
        required: true
    },
    password: {
        type: String,
        required: true
    }
});

module.exports=mongoose1.model('User',user);